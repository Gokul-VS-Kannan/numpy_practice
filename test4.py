# given two array a and b perform element wise addition,substraction,multiplication and division
import numpy as np

a = np.array([10,20,30,40,50])
b = np.array([1,2,3,4,5])

add = a + b
print("elementary addition of array is :",add)

sub = a - b
print("elementary substraction of array is:",sub)

mtl = a * b
print("elementary multiplication of array is :",mtl)

div = a / b
print("elementary division of arry is :",div)